export function getTemplate(id) {
    return templates.find(t => t.id === Number(id)) || null;
}

export function getRelatedTemplates(currentId, limit = 3) {
    const current = getTemplate(currentId);
    if (!current) return [];
    return templates
        .filter(t => t.id !== current.id && t.category === current.category)
        .slice(0, limit);
}

export function getTemplateBySlug(slug) {
    return templates.find(t => t.slug === slug) || null;
}

